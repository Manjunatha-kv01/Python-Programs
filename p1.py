# Python Program to check if a Number Is Positive Or Negative
num = 15 # You can change this value to test with different numbers
if num > 0:# Check if the number is greater than 0
    print("Positive number") # If the number is greater than 0, it is a positive number
elif num < 0: # Check if the number is less than 0
    print("Negative number") # If the number is less than 0, it is a negative number
else:# If the number is not greater than 0 and not less than 0, it must be zero
    print("Zero") # If the number is neither greater than 0 nor less than 0, it is zero

#output:positive number
